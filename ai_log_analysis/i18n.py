def tr(key: str, **kwargs: Any) -> str:
    template = TRANSLATIONS.get(CURRENT_LANG, TRANSLATIONS[DEFAULT_LANG]).get(key)
    if template is None:
        template = TRANSLATIONS[DEFAULT_LANG].get(key, key)
    return template.format(**kwargs)


def set_language(lang: str) -> None:
    global CURRENT_LANG
    normalized = (lang or DEFAULT_LANG).strip().lower()
    if normalized not in {"pl", "en"}:
        raise RuntimeError(tr("lang_invalid"))
    CURRENT_LANG = normalized
